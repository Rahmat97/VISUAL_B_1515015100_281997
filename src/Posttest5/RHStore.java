    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int baris = jTable1.getSelectedRow();
        
        jTable1.setValueAt(jTextField1.getText(), baris, 0);
        jTable1.setValueAt(jRadioButton1.isSelected(), baris, 1);
        jTable1.setValueAt(jTextField2.getText(), baris, 2);
        jTable1.setValueAt(jComboBox1.getSelectedItem(), baris, 3);
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int baris = jTable1.getSelectedRow();
        
        String nama_edit = jTable1.getValueAt(baris, 0).toString();
        String jeniskelamin_edit = jTable1.getValueAt(baris, 1).toString();
        String telepon_edit = jTable1.getValueAt(baris, 2).toString();
        String kota_edit = jTable1.getValueAt(baris, 0).toString();
        
        jTextField1.setText(nama_edit);
        jRadioButton1.setText(jeniskelamin_edit);
        jTextField2.setText(telepon_edit);
        jComboBox1.setSelectedItem(kota_edit);
        
    }//GEN-LAST:event_jTable1MouseClicked

